#!/usr/bin/env node
// Regenerates packages/tokens/src/primitive/color.json's 7 hue scales in OKLCH.
//
// Phase 1/2 generated these scales with plain HSL math, which isn't perceptually
// uniform — equal HSL lightness steps don't read as equal visual steps, especially
// through the midtones (this produced the real text.success AA failure fixed in
// Phase 3). OKLCH's L channel is designed to be perceptually uniform, so an evenly
// spaced L curve produces evenly spaced *visual* steps.
//
// Two scales carry an exact brand-mandated anchor hex (purple-600 = #5548A4,
// emerald-600 = #2E8A7D, per guidelines/01-vision-and-goals.md's decisions log) —
// for those, every other step is generated by shifting a shared canonical L/chroma
// curve so it passes exactly through the anchor's own OKLCH decomposition at 600.
// The other 5 scales (gray, red, amber, green, blue) have no anchor requirement and
// regenerate fully from the canonical curve, using their current 600 value only to
// pick a matching hue/peak-chroma so the scale keeps its existing color identity.
//
// Re-run this whenever a scale needs re-deriving (e.g. adding a 3rd brand theme):
//   node packages/tokens/scripts/generate-color-scales.mjs
//
// It overwrites src/primitive/color.json's hue scales; "neutral" (white/black) is
// left untouched since it isn't a hue-based scale.

import { converter, formatHex, clampChroma } from 'culori';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const COLOR_JSON_PATH = join(__dirname, '..', 'src', 'primitive', 'color.json');

const STEPS = ['50', '100', '200', '300', '400', '500', '600', '700', '800', '900', '950'];

// Perceptually-spaced OKLCH lightness per step, calibrated so 50 sits near white
// and 950 sits near black without hitting either extreme (pure white/black at the
// scale ends reads as harsh and loses the last bit of usable contrast range).
const CANONICAL_L = {
  '50': 0.985, '100': 0.955, '200': 0.9, '300': 0.825, '400': 0.74,
  '500': 0.65, '600': 0.56, '700': 0.47, '800': 0.38, '900': 0.29, '950': 0.2,
};

// Chroma relative to the step-600 value — saturation tapers toward both ends since
// the sRGB gamut's max chroma shrinks toward zero near white/black regardless of
// hue; clampChroma() below handles any step that's still out of gamut after this.
const CHROMA_MULTIPLIER = {
  '50': 0.05, '100': 0.12, '200': 0.32, '300': 0.55, '400': 0.8,
  '500': 0.95, '600': 1, '700': 0.9, '800': 0.72, '900': 0.55, '950': 0.38,
};

const toOklch = converter('oklch');

// Brand-anchored scales: exact hex required at 600 (guidelines/01-vision-and-goals.md
// decisions log). Every other step is the canonical L curve shifted by a constant
// offset so it passes through the anchor's own L at 600 — this preserves the
// curve's perceptual spacing while still hitting the required color exactly.
const ANCHORED_SCALES = {
  purple: '#5548A4',
  emerald: '#2E8A7D',
};

// Free scales: no anchor requirement. Use the canonical L curve directly, and pick
// hue/peak-chroma from the scale's current 600 value so it keeps its existing
// color identity (red stays red, etc.) while gaining OKLCH's even step spacing.
const FREE_SCALES = {
  gray: '#69657B',
  red: '#B62F2B',
  amber: '#C4871C',
  green: '#419F64',
  blue: '#3262AE',
};

function clampL(l) {
  return Math.min(0.99, Math.max(0.03, l));
}

function toHex({ l, c, h }) {
  const inGamut = clampChroma({ mode: 'oklch', l: clampL(l), c, h }, 'oklch', 'rgb');
  return formatHex(inGamut).toUpperCase();
}

function generateAnchoredScale(hex) {
  const anchor = toOklch(hex);
  const lOffset = anchor.l - CANONICAL_L['600'];
  const scale = {};
  for (const step of STEPS) {
    if (step === '600') {
      // Hardcoded rather than round-tripped through OKLCH conversion — guarantees
      // byte-identical anchor preservation rather than relying on float round-trip.
      scale[step] = hex.toUpperCase();
      continue;
    }
    const l = CANONICAL_L[step] + lOffset;
    const c = anchor.c * CHROMA_MULTIPLIER[step];
    scale[step] = toHex({ l, c, h: anchor.h });
  }
  return scale;
}

function generateFreeScale(hex) {
  const anchor = toOklch(hex);
  const scale = {};
  for (const step of STEPS) {
    const l = CANONICAL_L[step];
    const c = anchor.c * CHROMA_MULTIPLIER[step];
    scale[step] = toHex({ l, c, h: anchor.h });
  }
  return scale;
}

function toTokenShape(scale) {
  const out = {};
  for (const step of STEPS) {
    out[step] = { $type: 'color', $value: scale[step] };
  }
  return out;
}

const current = JSON.parse(readFileSync(COLOR_JSON_PATH, 'utf-8'));

const result = {
  purple: toTokenShape(generateAnchoredScale(ANCHORED_SCALES.purple)),
  emerald: toTokenShape(generateAnchoredScale(ANCHORED_SCALES.emerald)),
  gray: toTokenShape(generateFreeScale(FREE_SCALES.gray)),
  red: toTokenShape(generateFreeScale(FREE_SCALES.red)),
  amber: toTokenShape(generateFreeScale(FREE_SCALES.amber)),
  green: toTokenShape(generateFreeScale(FREE_SCALES.green)),
  blue: toTokenShape(generateFreeScale(FREE_SCALES.blue)),
  neutral: current.neutral,
};

writeFileSync(COLOR_JSON_PATH, JSON.stringify(result, null, 2) + '\n');

console.log('Regenerated', COLOR_JSON_PATH);
for (const [name, scale] of Object.entries(result)) {
  if (name === 'neutral') continue;
  console.log(`\n${name}:`);
  for (const step of STEPS) {
    console.log(`  ${step.padStart(3)}: ${scale[step].$value}`);
  }
}
